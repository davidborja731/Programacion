#La función range en Python se utiliza para generar una secuencia 
#de números. En este caso, (2, 21, 2) se utiliza para generar una 
# secuencia de números pares entre 1 y 20.
#El primer argumento 2 es el número inicial de la secuencia. 
# El segundo argumento 21 es el número final de la secuencia, 
# pero no se incluye en la secuencia generada. 
# El tercer argumento 2 es el incremento entre los números de la secuencia.
for x in range(2, 21, 2):
    print(x)

